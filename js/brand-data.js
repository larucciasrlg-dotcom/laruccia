function caricaBrand() {
  const params = new URLSearchParams(window.location.search);
  const brandKey = params.get("name");

  if (!brandKey || !brandData[brandKey]) {

  document.body.innerHTML = `
    <section class="brand-error">

      <div class="container">

        <h1>Brand non trovato</h1>

        <p>
          Il brand richiesto non è disponibile
          oppure è stato temporaneamente rimosso.
        </p>

        <a href="prodotti.html" class="btn-primary">
          Torna ai prodotti
        </a>

      </div>

    </section>
  `;

  return;
}

  const data = brandData[brandKey];
  data.categorie = data.categorie || brandCategories[brandKey] || [];

  document.title = `${data.nome} - Laruccia`;

  const title = document.getElementById("brand-title");
  const description = document.getElementById("brand-description");
  const mainImg = document.getElementById("brand-main-img");
  const link = document.getElementById("brand-link");
  const gallery = document.getElementById("brand-gallery");
  if (link) {
  link.href = data.sito;
  link.target = "_blank";
  link.rel = "noopener noreferrer";
}

const collectionsLink = document.getElementById("brand-collections-link");

if (collectionsLink) {
  collectionsLink.href = data.sito;
}

  if (title) title.innerText = data.nome;
  if (description) description.innerText = data.descrizione;

  if (mainImg) {
    mainImg.src = data.imgPrincipale;
    mainImg.alt = `Ambientazione ${data.nome}`;
  }

  if (link) {
    link.href = data.sito;
    link.target = "_blank";
    link.rel = "noopener noreferrer";
  }

  if (gallery && Array.isArray(data.gallery)) {
    gallery.innerHTML = "";

data.gallery.forEach(itemData => {
  const item = document.createElement("a");
  item.className = "gallery-item";

  const imgUrl = typeof itemData === "string" ? itemData : itemData.img;
  const nomeCollezione = typeof itemData === "string" ? data.nome : itemData.nome;
  const linkCollezione = typeof itemData === "string" ? data.sito : itemData.link;

  item.href = linkCollezione;
  item.target = "_blank";
  item.rel = "noopener noreferrer";

  item.innerHTML = `
    <img 
      src="${imgUrl}" 
      alt="${nomeCollezione} ${data.nome}" 
      loading="lazy">

    <div class="gallery-caption">
      <span>${nomeCollezione}</span>
    </div>
  `;

  gallery.appendChild(item);
});
  }
}